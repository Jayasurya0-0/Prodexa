import { jsPDF } from "jspdf";
import { ProductionLine } from "../types";

export function generateDailyYieldPDF(lines: ProductionLine[], date: string) {
  const doc = new jsPDF({
    orientation: "portrait",
    unit: "mm",
    format: "a4",
  });

  const totalLines = lines.length;
  const totalTarget = lines.reduce((acc, l) => acc + l.targetPcs, 0);
  const totalProduction = lines.reduce((acc, l) => acc + l.currentProductionPcs, 0);
  const avgEfficiency = totalLines > 0 
    ? lines.reduce((acc, l) => acc + l.efficiency, 0) / totalLines 
    : 0;

  // Header Design
  doc.setFillColor(15, 23, 42); // slate-900
  doc.rect(0, 0, 210, 40, "F");

  doc.setTextColor(255, 255, 255);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(22);
  doc.text("PRODEXA INC.", 15, 18);
  
  doc.setFont("helvetica", "normal");
  doc.setFontSize(10);
  doc.setTextColor(148, 163, 184); // slate-400
  doc.text("INDUSTRIAL SEWING FLOOR YIELD SYSTEMS", 15, 24);

  doc.setFont("helvetica", "bold");
  doc.setFontSize(11);
  doc.setTextColor(16, 185, 129); // emerald-500
  doc.text(`REPORT: DAILY YIELD & OUTPUT SUMMARY`, 15, 33);

  // Date and Metadata on the right
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(10);
  doc.setFont("helvetica", "bold");
  doc.text(`DATE: ${date}`, 145, 18);
  doc.setFont("helvetica", "normal");
  doc.setTextColor(148, 163, 184);
  doc.text(`SHIFT: DAY SHIFT (10 HR)`, 145, 24);
  doc.text(`STATUS: ACTIVE FLOORS`, 145, 30);

  // Summary Cards Section
  doc.setTextColor(15, 23, 42);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(12);
  doc.text("FACTORY FLOOR PERFORMANCE SUMMARY", 15, 52);

  // Draw 4 cards side by side
  const cards = [
    { label: "TOTAL SEWING LINES", val: totalLines.toString() },
    { label: "TARGET QUANTITY (PCS)", val: totalTarget.toLocaleString() },
    { label: "CURRENT PRODUCTION", val: totalProduction.toLocaleString() },
    { label: "AVERAGE EFFICIENCY", val: `${avgEfficiency.toFixed(1)}%` }
  ];

  cards.forEach((card, i) => {
    const x = 15 + i * 46;
    doc.setFillColor(248, 250, 252); // slate-50
    doc.setDrawColor(226, 232, 240); // slate-200
    doc.roundedRect(x, 58, 42, 20, 2, 2, "FD");

    doc.setFont("helvetica", "normal");
    doc.setFontSize(7);
    doc.setTextColor(100, 116, 139); // slate-500
    doc.text(card.label, x + 3, 64);

    doc.setFont("helvetica", "bold");
    doc.setFontSize(12);
    doc.setTextColor(15, 23, 42);
    doc.text(card.val, x + 3, 72);
  });

  // Detailed Table
  doc.setFont("helvetica", "bold");
  doc.setFontSize(11);
  doc.setTextColor(15, 23, 42);
  doc.text("LINE-WISE OUTPUT breakdown", 15, 90);

  // Table header
  let y = 96;
  doc.setFillColor(15, 23, 42);
  doc.rect(15, y, 180, 8, "F");

  doc.setTextColor(255, 255, 255);
  doc.setFontSize(8);
  doc.setFont("helvetica", "bold");
  doc.text("LINE", 17, y + 5.5);
  doc.text("SUPERVISOR", 35, y + 5.5);
  doc.text("BUYER", 75, y + 5.5);
  doc.text("STYLE", 100, y + 5.5);
  doc.text("TARGET (PCS)", 130, y + 5.5);
  doc.text("OUTPUT (PCS)", 155, y + 5.5);
  doc.text("EFFICIENCY", 180, y + 5.5);

  lines.forEach((line) => {
    y += 8;
    doc.setFillColor(255, 255, 255);
    doc.rect(15, y, 180, 8, "F");
    
    // Draw thin light borders
    doc.setDrawColor(241, 245, 249);
    doc.line(15, y + 8, 195, y + 8);

    doc.setFont("helvetica", "bold");
    doc.setTextColor(37, 99, 235); // blue-600
    doc.text(line.lineNo, 17, y + 5.5);

    doc.setFont("helvetica", "normal");
    doc.setTextColor(15, 23, 42);
    doc.text(line.supervisor.substring(0, 18), 35, y + 5.5);
    doc.text(line.buyer.substring(0, 12), 75, y + 5.5);
    doc.text(line.style.substring(0, 12), 100, y + 5.5);

    doc.setFont("helvetica", "bold");
    doc.text(line.targetPcs.toLocaleString(), 130, y + 5.5);
    doc.text(line.currentProductionPcs.toLocaleString(), 155, y + 5.5);

    // Color efficiency
    if (line.efficiency >= 85) {
      doc.setTextColor(16, 185, 129); // emerald-500
    } else if (line.efficiency >= 60) {
      doc.setTextColor(37, 99, 235); // blue-600
    } else {
      doc.setTextColor(245, 158, 11); // amber-500
    }
    doc.text(`${line.efficiency.toFixed(1)}%`, 180, y + 5.5);
  });

  // Footer
  y += 20;
  doc.setDrawColor(226, 232, 240);
  doc.line(15, y, 195, y);
  
  doc.setFont("helvetica", "normal");
  doc.setFontSize(8);
  doc.setTextColor(148, 163, 184);
  doc.text("Generated by Prodexa MES • Confidential Floor Report", 15, y + 6);
  doc.text(`Page 1 of 1`, 180, y + 6);

  doc.save(`Prodexa_Daily_Yield_Report_${date}.pdf`);
}

